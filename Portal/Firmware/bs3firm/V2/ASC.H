//****************************************************************************
// @Macro          ASC_vReceiverDisable()
//
//----------------------------------------------------------------------------
// @Description   This macro disables the receive function of the ASC0
//                channel. A data that is currently being received is 
//                received to completion, including generation of the 
//                receive interrupt request.
//
//----------------------------------------------------------------------------
// @Returnvalue   none
//
//----------------------------------------------------------------------------
// @Parameters    none
//
//----------------------------------------------------------------------------
// @Date          18.09.98 10:01:30
//
//****************************************************************************

#define ASC_vReceiverDisable() S0REN = 0


//****************************************************************************
// @Macro          ASC_vSetBaudrate(Value)
//
//----------------------------------------------------------------------------
// @Description   This macro loads the reload register of the baudrate
//                generator. The baudrate generator is loaded immediately
//                with the new value.
//
//----------------------------------------------------------------------------
// @Returnvalue   none
//
//----------------------------------------------------------------------------
// @Parameters    13 bit value for the baudrate reload register
//
//----------------------------------------------------------------------------
// @Date          18.09.98 10:01:30
//
//****************************************************************************

#define ASC_vSetBaudrate(Value) S0BG = Value


//****************************************************************************
// @Macro          ASC_vStopBaudGen()
//
//----------------------------------------------------------------------------
// @Description   This macro stops the baudrate generator (ASC inactive).
//
//----------------------------------------------------------------------------
// @Returnvalue   none
//
//----------------------------------------------------------------------------
// @Parameters    none
//
//----------------------------------------------------------------------------
// @Date          18.09.98 10:01:30
//
//****************************************************************************

#define ASC_vStopBaudGen() S0R = 0


//****************************************************************************
// @Macro          ASC_vStartBaudGen()
//
//----------------------------------------------------------------------------
// @Description   This macro starts the baudrate generator (ASC active).
//
//----------------------------------------------------------------------------
// @Returnvalue   none
//
//----------------------------------------------------------------------------
// @Parameters    none
//
//----------------------------------------------------------------------------
// @Date          18.09.98 10:01:30
//
//****************************************************************************

#define ASC_vStartBaudGen() S0R = 1



